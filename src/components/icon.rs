#[derive(Clone)]
pub struct Icon{
	pub icon: char,
	pub size: f32,
	pub color: Color,
}

impl Icon{
	pub fn new(ic: char) -> Icon{
		Icon{
			icon: ic,
			..Default::default()
		}
	}
}
impl Default for Icon{
	fn default() -> Icon{
		Icon{
			icon: ' ',
			size: 16.,
			color: Color::rgb(255,255,255),
		}
	}
}

impl Widget for Icon{
	type Event = ();
	type State = ();

	fn render<C:Context<TWidget=Icon>>(&self, c: &mut C) {
		c.font_face("font-awesome");
		c.draw(|be| be.font_size(self.size) );
		let mut s = String::new();
		s.push(self.icon);
		c.text(10.,10., &s[..]);
	}
	fn init(){
		//TODO: load the font
	}
}

